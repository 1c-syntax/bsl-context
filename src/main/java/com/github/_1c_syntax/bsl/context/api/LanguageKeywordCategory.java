package com.github._1c_syntax.bsl.context.api;

/**
 * Подкатегория языковой конструкции встроенного языка
 * (детализация для {@link ContextKind#LANGUAGE_KEYWORD}).
 *
 * <p>Соответствует разделам синтакс-помощника «Встроенный язык».
 */
public enum LanguageKeywordCategory {
    /** Литерал значения примитивного типа: {@code Истина}, {@code Ложь}. */
    LITERAL,
    /**
     * Управляющая конструкция: {@code Если...Тогда...КонецЕсли},
     * {@code Для...Цикл}, {@code Пока...Цикл}, {@code Попытка...Исключение}.
     */
    STATEMENT,
    /**
     * Оператор языка: {@code Новый}, {@code Выполнить}, тернарник
     * {@code ?(...)}, оператор присваивания, {@code Ждать}.
     */
    OPERATOR,
    /** Объявление: {@code Перем}, {@code Процедура}, {@code Функция}. */
    DECLARATION,
    /**
     * Директива компиляции для процедур/функций модулей форм и общих модулей:
     * {@code &НаКлиенте}, {@code &НаСервере}, {@code &НаСервереБезКонтекста}
     * и др. Предваряется символом {@code &}.
     */
    PRAGMA,
    /**
     * Аннотация заимствованного метода в расширении конфигурации:
     * {@code &Перед}, {@code &После}, {@code &Вместо},
     * {@code &ИзменениеИКонтроль}.
     */
    ANNOTATION,
    /**
     * Инструкция препроцессора: {@code #Если}, {@code #Тогда},
     * {@code #ИначеЕсли}, {@code #КонецЕсли}, {@code #Область},
     * {@code #КонецОбласти}, {@code #Вставка}, {@code #Удаление}.
     */
    PREPROCESSOR_INSTRUCTION
}
