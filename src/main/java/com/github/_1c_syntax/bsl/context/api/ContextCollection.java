package com.github._1c_syntax.bsl.context.api;

import java.util.List;

/**
 * Платформенная коллекция значений — {@code Массив}, {@code Соответствие},
 * {@code Структура}, {@code ТаблицаЗначений}, {@code СписокЗначений},
 * {@code ДеревоЗначений} и т.п. Имеет полный набор членов
 * {@link ContextType} (методы / свойства / события / конструкторы) и
 * дополнительно — описание типа элементов через
 * {@link #collectionElementTypes()}.
 * <p>
 * Источник информации об элементах — блок «Элементы коллекции:» на
 * главной HTML-странице типа в {@code shcntx_*.hbk}. Если такого блока
 * нет — мы публикуем тип как обычный {@link ContextType}, а не как
 * коллекцию.
 */
public interface ContextCollection extends ContextType {

    /**
     * Типы элементов коллекции. Для большинства коллекций здесь один
     * тип (например, для {@code Соответствие} — {@code КлючИЗначение}),
     * но в редких случаях может быть несколько вариантов.
     */
    List<Context> collectionElementTypes();

    /**
     * Признак того, что для коллекции доступен обход в цикле
     * {@code Для каждого … Из … Цикл}. Извлекается из текста блока
     * «Элементы коллекции:» по упоминанию «Для каждого».
     */
    default boolean supportsForEach() {
        return false;
    }

    /**
     * Описание поведения обхода «Для каждого» — что именно выбирается
     * при обходе (значения, элементы, строки). Если обход не поддерживается,
     * пустая строка.
     */
    default String forEachDescription() {
        return "";
    }

    /**
     * Признак того, что для коллекции доступен индексатор {@code [...]}.
     * Извлекается из текста по упоминанию «оператора [...]».
     */
    default boolean supportsIndexAccess() {
        return false;
    }

    /**
     * Описание поведения индексатора — что передаётся аргументом
     * (индекс с какого числа, имя ключа и т.п.). Если индексатор не
     * поддерживается, пустая строка.
     */
    default String indexAccessDescription() {
        return "";
    }
}
